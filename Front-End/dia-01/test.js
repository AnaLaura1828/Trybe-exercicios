const uppercase = (str, callback) => {
  setTimeout(() => {
    callback(str.toUpperCase());
  }, 500);
};

it('Teste para ver se "todas" as letras são "MAIUSCULAS"', (done) => {
    uppercase('todas', (str) => {
        try {
            expect(str).toBe('TODAS');
            done();
        } catch (error) {
            done(error);
        }
    });
});

